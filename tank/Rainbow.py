# Perform color correction but ignore temperature adjustment
def color_correct(pixel, scale):
	return (
		(pixel[0] * (scale[0] + 1)) >> 8,
		(pixel[1] * (scale[1] + 1)) >> 8,
		(pixel[2] * (scale[2] + 1)) >> 8,
	)

